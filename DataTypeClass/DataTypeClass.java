package DataTypeClass;

public class DataTypeClass {
    public static void main(String[] args) {
        createTypes();
    }

    public static void createTypes() {
        int i = 4;
        double d = 4.0213D;
        float f = 4.0213F;
        char c = 'c';
        boolean b = true;

        System.out.print("Integer: " + i + " Double: " + d + " Float: " + f + " Char: " + c + " Boolean: " + b);
    }
}
